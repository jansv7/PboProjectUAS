    private void pnl_kembalikanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnl_kembalikanMouseClicked
        // TODO add your handling code here:
        String username = txtUsername.getText().trim();
        String judulDipilih = cbOpsi1.getSelectedItem().toString();

        if (username.isEmpty() || judulDipilih == null) {
            JOptionPane.showMessageDialog(this, 
                    "Isi Username dan pilih buku!", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        PeminjamanDAO dao = new PeminjamanDAO();
        boolean sukses = dao.kembalikanBuku(username, judulDipilih);

        if (sukses) {
            JOptionPane.showMessageDialog(this, 
                    "Buku berhasil dikembalikan!", 
                    "Sukses", 
                    JOptionPane.INFORMATION_MESSAGE);

            // Refresh ComboBox agar buku yang sudah dikembalikan hilang dari daftar
            loadBorrowedBooks();

        } else {
        JOptionPane.showMessageDialog(this, 
                "Pengembalian gagal! Pastikan username dan buku valid.", 
                "Gagal", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_pnl_kembalikanMouseClicked

