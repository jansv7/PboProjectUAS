    private void loadBorrowedBooks() {
    String username = txtUsername.getText().trim();

    if (username.isEmpty()) return;

    PeminjamanDAO pdao = new PeminjamanDAO();
    List<String> borrowedBooks = pdao.getBorrowedBooksByUsername(username);

    cbOpsi1.removeAllItems();

    for (String judul : borrowedBooks) {
        cbOpsi1.addItem(judul);
    }
}


